public class EjemploValoresVariables {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int numero1 = 5;
        int numero2 = 9;
        System.out.println(numero2);
        numero2 = numero1;
        System.out.println(numero2);
        numero1 = 88;
        System.out.println(numero2);
    }
    
}
